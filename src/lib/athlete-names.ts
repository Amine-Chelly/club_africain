export function buildAthleteName(index: number, gender: "MALE" | "FEMALE", salt: string) {
  const firstNames = gender === "FEMALE" ? femaleFirstNames : maleFirstNames;
  const first = firstNames[index % firstNames.length];
  const last = lastNames[(index * 3 + salt.length) % lastNames.length];
  return `${first} ${last}`;
}
